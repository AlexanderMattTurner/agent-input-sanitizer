#!/usr/bin/env node
/**
 * Generate the language-agnostic invisible-charset SSOT from invisible.mjs plus
 * Node's Unicode data.
 *
 * The authoritative definition of the payload-capable invisible characters lives
 * in `src/invisible.mjs` (the `VS` and `BLANK_NON_CF` exports) plus the Unicode
 * general-category `Cf`. A non-JS consumer — e.g. the Python `agent-secret-redactor`
 * engine — cannot import that module, and must NOT hand-copy the set: a fork is a
 * silent security regression (a code point added here but not there lets a key
 * spliced with it escape one layer).
 *
 * `Cf` used to be resolved LIVE by each consumer from its own Unicode database,
 * but Node (U17) and the CPython interpreter (often U14/U15) ship DIFFERENT
 * Unicode versions, so the two layers stripped DIFFERENT `Cf` sets — a key
 * spliced with a code point in the version delta (e.g. U+13439) escaped the layer
 * on the older Unicode. So `Cf` is now PINNED here at generation time (from
 * Node's `\p{Cf}`) alongside the non-Cf extras, and every consumer reads the
 * pinned list instead of resolving `Cf` live. Version-locking the set is what
 * makes the JS layer and the Python port strip an identical set regardless of
 * each runtime's own Unicode version.
 *
 * Writes two generated artifacts, both from the SAME pinned data so they cannot
 * drift:
 *   - `python/agent_input_sanitizer/data/invisible-charset.json` (packaged with
 *     the Python client; read by `agent_input_sanitizer.invisible`).
 *   - `src/cf-charset.mjs` (shipped in the npm package; read by `src/invisible.mjs`).
 * Run from the repo root: `node scripts/gen-invisible-charset.mjs`.
 * `test/invisible-charset.test.mjs` fails CI if either committed artifact drifts
 * from this output.
 */
import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

import { VS, BLANK_NON_CF } from "../src/invisible.mjs";

const __dirname = dirname(fileURLToPath(import.meta.url));

/** Sorted, de-duped code points of every char in the given strings. */
export function extraCodepoints() {
  const cps = new Set();
  for (const s of [VS, BLANK_NON_CF])
    for (const ch of s) cps.add(ch.codePointAt(0));
  return [...cps].sort((a, b) => a - b);
}

/** Every general-category `Cf` code point in THIS Node's Unicode data, sorted
 * ascending. This is the pinned set both layers read (see the header): resolving
 * it once at generation time is what version-locks JS and Python to an identical
 * `Cf` set. */
export function cfCodepoints() {
  const isCf = /\p{Cf}/u;
  const cps = [];
  for (let cp = 0; cp <= 0x10ffff; cp++)
    if (isCf.test(String.fromCodePoint(cp))) cps.push(cp);
  return cps;
}

/** The Unicode version the pinned `Cf` set was generated from (Node's). */
export function unicodeVersion() {
  return process.versions.unicode;
}

export function charsetDoc() {
  return {
    _comment:
      "SSOT for the payload-capable invisible code points, generated by " +
      "scripts/gen-invisible-charset.mjs. `extra_codepoints` are the non-Cf extras " +
      "(variation selectors, blank-rendering fillers, zero-width combining marks) " +
      "from src/invisible.mjs (VS + BLANK_NON_CF). `cf_codepoints` is the general-" +
      "category Cf set PINNED from Node's Unicode data at generation time (see " +
      "`unicode_version`) — NOT resolved live per consumer, because Node and CPython " +
      "ship different Unicode versions and a live-Cf split let a code point in the " +
      "version delta escape one layer. The deletion set is the UNION of the two " +
      "lists. Consumers in other languages read this file instead of forking the " +
      "list — a fork is a silent security regression.",
    unicode_version: unicodeVersion(),
    extra_codepoints: extraCodepoints(),
    cf_codepoints: cfCodepoints(),
  };
}

/** The generated `src/cf-charset.mjs` source: the pinned `Cf` set the JS layer
 * reads. A generated `src/` module (not a read of the Python-packaged JSON) so
 * it ships in the npm package, matching src/standardized-variants.mjs. */
export function cfCharsetModule() {
  const cps = cfCodepoints();
  const hex = cps.map((cp) => `0x${cp.toString(16)}`).join(", ");
  return `/**
 * GENERATED by scripts/gen-invisible-charset.mjs from Node's Unicode data
 * (\\p{Cf}, Unicode ${unicodeVersion()}) — DO NOT EDIT.
 *
 * The general-category Cf code points, PINNED at generation time. invisible.mjs
 * strips exactly this set instead of testing \\p{Cf} live, and the Python port
 * reads the SAME set from data/invisible-charset.json's \`cf_codepoints\`, so both
 * layers strip an identical Cf set regardless of each runtime's own Unicode
 * version. Regenerate with \`node scripts/gen-invisible-charset.mjs\`;
 * test/invisible-charset.test.mjs fails if this drifts from Node's \\p{Cf}.
 */

export const UNICODE_VERSION = "${unicodeVersion()}";

// GENERATED Unicode data, not hand-written logic. Its correctness is pinned by
// the SSOT round-trip in test/invisible-charset.test.mjs (re-enumerate \\p{Cf},
// then assert every code point matches) — Stryker skips the numeric literals.
// Stryker disable all
/** @type {readonly number[]} Sorted ascending. */
export const CF_CODEPOINTS = Object.freeze([${hex}]);
// Stryker restore all
`;
}

export const OUTPUT_PATH = join(
  __dirname,
  "..",
  "python",
  "agent_input_sanitizer",
  "data",
  "invisible-charset.json",
);

export const CF_MODULE_PATH = join(__dirname, "..", "src", "cf-charset.mjs");

function main() {
  writeFileSync(OUTPUT_PATH, JSON.stringify(charsetDoc(), null, 2) + "\n");
  writeFileSync(CF_MODULE_PATH, cfCharsetModule());
  console.log(
    `wrote ${OUTPUT_PATH} (${extraCodepoints().length} extra + ${cfCodepoints().length} Cf code points)`,
  );
  console.log(`wrote ${CF_MODULE_PATH}`);
}

if (process.argv[1] === fileURLToPath(import.meta.url)) main();
